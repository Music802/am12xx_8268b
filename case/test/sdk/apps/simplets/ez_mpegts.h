typedef struct AVCodecContext{
    enum CodecType codec_type; /* see CODEC_TYPE_xxx */    
    enum CodecID codec_id; /* see CODEC_ID_xxx */
    unsigned int codec_tag;
}AVCodecContext;	


typedef struct AVPacket {
    int64_t pts;
    int64_t dts;
    unsigned char *data;
    unsigned int bus_addr;
    int   size;
    int   stream_index;
    int   flags;
    int   duration;
    void  (*destruct)(struct AVPacket *pkt);	
    void *mp_handle;
    void  *priv;
    int64_t pos; 
    int64_t convergence_duration;
    int rv_slice_cnt;
} AVPacket;

typedef struct _AVRational_
{
    int num; ///< numerator
    int den; ///< denominator
}AVRational;


typedef struct AVStream
{
    int index;  
    int id;
    int pmt_id;
    AVRational time_base;
    void *priv_data;
    AVCodecContext *codec;
    uint32_t request_probe:1,
        xx:31;
    int64_t start_time;
    int64_t duration;
    int64_t nb_frames;
    int64_t cur_dts;
}AVStream;


typedef struct AVFormatContext {
    unsigned int nb_streams;
    int audio_stream_index;
    int video_stream_index;
    AVStream *streams[MAX_STREAMS];
    ByteIOContext *pb;
    int64_t probesize;
    int64_t start_time;   
    int64_t duration;
    void *priv_data;
}AVFormatContext;


int mpegts_read_header(AVFormatContext **av, char * const name, av_file_iocontext_s *fio);
